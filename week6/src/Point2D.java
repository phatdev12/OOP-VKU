public class Point2D {
    private double x;
    private double y;

    // Hàm khởi tạo
    public Point2D(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // Hàm lấy giá trị x
    public double getX() {
        return x;
    }

    // Hàm lấy giá trị y
    public double getY() {
        return y;
    }
}