import java.util.Scanner;
public class Number_1 {
    public static void main(String[] args) {

        int number = 1;

        // Sử dụng vòng lặp while để in các số từ 1 đến 100
        while (number <= 100) {
            System.out.println(number);// In số hiện tại
            number += 1; // Tăng số lên 1
        }
    }
}
